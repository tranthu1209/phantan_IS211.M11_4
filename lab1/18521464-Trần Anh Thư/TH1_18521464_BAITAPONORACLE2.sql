--1. Tạo user tên BAITHI gồm có 4 table USER, CHANNEL, VIDEO, SHARE. Tạo khóa chính, khóa ngoại cho các table đó.--
--Tạo bảng USER_NEW--
CREATE TABLE BTCNTT2.USER_NEW
(
    U_ID VARCHAR2(3) CONSTRAINT PK_USER PRIMARY KEY,
    USERNAME VARCHAR2(50),
    PASS VARCHAR2(50),
    REGDAY DATE,
    NATIONALITY NVARCHAR2(50)
)

--Tạo bảng CHANNEL--
CREATE TABLE BTCNTT2.CHANNEL
(
    CHANNELID VARCHAR2(4) CONSTRAINT PK_CHANNEL PRIMARY KEY,
    CNAME NVARCHAR2(50),
    SUBSCRIBES NUMBER,
    OWNNER VARCHAR2(3),
    CREATED DATE
);
--Tạo bảng VIDEO--
CREATE TABLE BTCNTT2.VIDEO
(
    VIDEOID VARCHAR2(7) CONSTRAINT PK_VIDEO PRIMARY KEY,
    TITLE NVARCHAR2(50),
    DURATION NUMBER,
    AGE NUMBER
);
--Tạo bảng SHARES--
CREATE TABLE BTCNTT2.SHARES
(
    VIDEOID VARCHAR2(7),
    CHANNELID VARCHAR2(4),
    CONSTRAINT PK_SHARE PRIMARY KEY(VIDEOID, CHANNELID)
);

