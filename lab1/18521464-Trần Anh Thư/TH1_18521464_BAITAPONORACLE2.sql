--5. Tìm tất cả các video có giới hạn độ tuổi từ 16 trở lên.--
SELECT *
FROM BTCNTT2.VIDEO
WHERE AGE >= 16;

--6. Tìm kênh có số người theo dõi nhiều nhất*--
SELECT *
FROM BTCNTT2.CHANNEL
WHERE SUBSCRIBES = (
    SELECT MAX(SUBSCRIBES)
    FROM BTCNTT2.CHANNEL
);

--7. Với mỗi video có giới hạn độ tuổi là 18, thống kê số kênh đã chia sẻ.--
SELECT BTCNTT2.VIDEO.VIDEOID, COUNT(CHANNELID) AS SOLUONG
FROM BTCNTT2.VIDEO INNER JOIN BTCNTT2.SHARES ON VIDEO.VIDEOID = SHARES.VIDEOID
WHERE AGE >=18
GROUP BY BTCNTT2.VIDEO.VIDEOID;

--8. Tìm video được tất cả các kênh chia sẻ.--
SELECT * 
FROM BTCNTT2.VIDEO V
WHERE NOT EXISTS(
    SELECT *
    FROM  BTCNTT2.CHANNEL C
    WHERE NOT EXISTS(
        SELECT * 
        FROM BTCNTT2.SHARES S
        WHERE V.VIDEOID = S.VIDEOID 
        AND C.CHANNELID = S.CHANNELID
    )
)

